#define RM690_N(t)   ((uint16_t)(sizeof(t) / sizeof(struct rm690_cmd)))

/* Panel variant lookup: RDID -> init table + CASET X-offset (panel addressable origin). */
struct rm690_variant {
    uint32_t id;                      /* RDID = (0xDA<<16)|(0xDB<<8)|0xDC */
    const struct rm690_cmd *init;
    uint16_t count;
    uint16_t x_offset;                /* CASET X start */
    const char *name;
};
